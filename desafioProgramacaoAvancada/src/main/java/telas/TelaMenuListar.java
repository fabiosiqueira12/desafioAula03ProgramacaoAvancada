    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Listar Funcionários
        RepositorioPessoa repositorio = null;
        try {
            repositorio = RepositorioPessoa.getInstancia();
        } catch (ParseException ex) {
            Logger.getLogger(TelaMenuListar.class.getName()).log(Level.SEVERE, null, ex);
        }
        List<Funcionario> listaFuncionarios = repositorio.getFuncionarios();
        
        if (listaFuncionarios.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Lista vazia!");
        } else {
            StringBuilder lista = new StringBuilder();

            for (Funcionario funcionario : listaFuncionarios) {
                //System.out.println(funcionario);
                lista.append(funcionario+"\n");
            }

            JOptionPane.showMessageDialog(rootPane, lista.toString());
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

