    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Listar Produtos
        RepositorioProduto repositorio = RepositorioProduto.getInstancia();
        List<Produto> listaProduto = repositorio.getProdutos();
        
        if (listaProduto.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Lista Vazia");
            
        }else {
            StringBuilder lista = new StringBuilder();
            
            for (Produto produto : listaProduto) {
                lista.append(produto+"\n");
            }
            
            JOptionPane.showMessageDialog(rootPane, lista.toString());
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

